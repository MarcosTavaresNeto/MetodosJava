package com.company;

public class Calculadora {

    public static double soma(double vlr01, double vlr02) {
        return vlr01 + vlr02;
    }

    public static double subtracao(double vlr01, double vlr02) {
        return vlr01 - vlr02;
    }

    public static double multiplicacao(double vlr01, double vlr02) {
        return vlr01 * vlr02;
    }

    public static double divisao(double vlr01, double vlr02) {
        return vlr01 / vlr02;
    }

}
