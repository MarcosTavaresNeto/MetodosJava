package com.company;

public class CalculoArea {

    public static double calcula(double lado){
        return lado*lado;
    }
    public static double calcula(double lado1, double lado2){
        return lado1*lado2;
    }
    public static double calcula(double lado1, double lado2, double lado3){
        return lado1*lado2*lado3;
    }
}
